#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*result;
	int		i;

	if (!s1)
		return (NULL);
	i = ft_strlen(s1);
	result = (char *)malloc(sizeof(char) * (i + 1));
	if (!result)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		result[i] = s1[i];
		i++;
	}
	result[i] = '\0';
	return (result);
}
